# Function to send email via SMTP
send_alert() {
    local subject="$1"
    local body="$2"
    local from="$3"
    local severity="$4"
    
    echo -e "${YELLOW}ðŸ“§ Sending: ${subject}${NC}"
    
    # Create email content and send via netcat
    (
        echo "HELO localhost"
        echo "MAIL FROM:<$from>"
        echo "RCPT TO:<$TO_EMAIL>"
        echo "DATA"
        echo "Subject: $subject"
        echo "From: $from"
        echo "To: $TO_EMAIL"
        echo "Date: $(date -R)"
        echo "Content-Type: text/plain"
        echo ""
        echo "ALERT SEVERITY: $severity"
        echo "TIMESTAMP: $(date)"
        echo ""
        echo "$body"
        echo ""
        echo "---"
        echo "Generated by Alert Summarizer Test Suite"
        echo "Timestamp: $(date)"
        echo "."
        echo "QUIT"
    ) | nc $MAILPIT_HOST $MAILPIT_PORT > /dev/null 2>&1
    
    if [ $? -eq 0 ]; then
        echo -e "${GREEN}âœ… Alert sent${NC}"
    else
        echo -e "${RED}âŒ Failed to send alert${NC}"
